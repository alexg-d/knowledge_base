def play():

    nums = set(i for i in range(1, int(input()) + 1))
    possible = nums

    while True:
        guess = set()
        for elem in input().split():
            if elem == 'HELP':
                return possible
            guess.add(int(elem))
        
        if len(guess.intersection(possible)) <= len(possible) / 2:
            print('NO')
            possible -= guess
        elif len(guess.intersection(possible)) > len(possible) / 2:
            print('YES')
            possible &= guess
        # print(possible)
            
        
                
            
print( *sorted(play(), key=int) )

